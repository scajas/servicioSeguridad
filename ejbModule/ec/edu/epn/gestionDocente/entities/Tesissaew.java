package ec.edu.epn.gestionDocente.entities;

import java.io.Serializable;
import javax.persistence.*;


/**
 * The persistent class for the tesissaew database table.
 * 
 */
@Entity
@Table(name="tesissaew", catalog = "bddcorpepn", schema = "`GestionDocente`")


public class Tesissaew implements Serializable {
	private static final long serialVersionUID = 1L;

	@Id
	private Integer idtesis;

	private String carrera;

	private String codcar;

	private String codigoestudiante;

	private String codigoprofe;

	private String defensaoral;

	private String facultad;

	private Integer idrecategorizacion;

	private String periodogrado;

	private String tesis;
	
	private String cateogoria;
	private String proceso;
	private String nivel;

    public Tesissaew() {
    }

	public Integer getIdtesis() {
		return this.idtesis;
	}

	public void setIdtesis(Integer idtesis) {
		this.idtesis = idtesis;
	}

	public String getCarrera() {
		return this.carrera;
	}

	public void setCarrera(String carrera) {
		this.carrera = carrera;
	}

	public String getCodcar() {
		return this.codcar;
	}

	public void setCodcar(String codcar) {
		this.codcar = codcar;
	}

	public String getCodigoestudiante() {
		return this.codigoestudiante;
	}

	public void setCodigoestudiante(String codigoestudiante) {
		this.codigoestudiante = codigoestudiante;
	}

	public String getCodigoprofe() {
		return this.codigoprofe;
	}

	public void setCodigoprofe(String codigoprofe) {
		this.codigoprofe = codigoprofe;
	}

	public String getDefensaoral() {
		return this.defensaoral;
	}

	public void setDefensaoral(String defensaoral) {
		this.defensaoral = defensaoral;
	}

	public String getFacultad() {
		return this.facultad;
	}

	public void setFacultad(String facultad) {
		this.facultad = facultad;
	}

	public Integer getIdrecategorizacion() {
		return this.idrecategorizacion;
	}

	public void setIdrecategorizacion(Integer idrecategorizacion) {
		this.idrecategorizacion = idrecategorizacion;
	}

	public String getPeriodogrado() {
		return this.periodogrado;
	}

	public void setPeriodogrado(String periodogrado) {
		this.periodogrado = periodogrado;
	}

	public String getTesis() {
		return this.tesis;
	}

	public void setTesis(String tesis) {
		this.tesis = tesis;
	}

	

	public String getProceso() {
		return proceso;
	}

	public void setProceso(String proceso) {
		this.proceso = proceso;
	}

	public String getCateogoria() {
		return cateogoria;
	}

	public void setCateogoria(String cateogoria) {
		this.cateogoria = cateogoria;
	}

	public String getNivel() {
		return nivel;
	}

	public void setNivel(String nivel) {
		this.nivel = nivel;
	}

}