package ec.edu.epn.acreditacion.DTO;

import java.io.Serializable;


public class IndicadorDetalleDTO implements Serializable{

	
	/**
	 * 
	 */
	private static final long serialVersionUID = 3574385754496084840L;
	private Integer idIndicadorDetalle;
	private Integer idIndicador;
	private Integer idSubCriterioDetalle;
	private String tipo;
	private String nombreIndicador;
	private Double peso;
	private Double calificacion;

	public IndicadorDetalleDTO() {
	}

	/**
	 * @return the idIndicadorDetalle
	 */
	public Integer getIdIndicadorDetalle() {
		return idIndicadorDetalle;
	}

	/**
	 * @param idIndicadorDetalle the idIndicadorDetalle to set
	 */
	public void setIdIndicadorDetalle(Integer idIndicadorDetalle) {
		this.idIndicadorDetalle = idIndicadorDetalle;
	}

	/**
	 * @return the idIndicador
	 */
	public Integer getIdIndicador() {
		return idIndicador;
	}

	/**
	 * @param idIndicador the idIndicador to set
	 */
	public void setIdIndicador(Integer idIndicador) {
		this.idIndicador = idIndicador;
	}

	/**
	 * @return the nombreIndicador
	 */
	public String getNombreIndicador() {
		return nombreIndicador;
	}

	/**
	 * @param nombreIndicador the nombreIndicador to set
	 */
	public void setNombreIndicador(String nombreIndicador) {
		this.nombreIndicador = nombreIndicador;
	}

	

	/**
	 * @return the idSubCriterioDetalle
	 */
	public Integer getIdSubCriterioDetalle() {
		return idSubCriterioDetalle;
	}

	/**
	 * @param idSubCriterioDetalle the idSubCriterioDetalle to set
	 */
	public void setIdSubCriterioDetalle(Integer idSubCriterioDetalle) {
		this.idSubCriterioDetalle = idSubCriterioDetalle;
	}

	/**
	 * @return the tipo
	 */
	public String getTipo() {
		return tipo;
	}

	/**
	 * @param tipo the tipo to set
	 */
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	/**
	 * @return the calificacion
	 */
	public Double getCalificacion() {
		return calificacion;
	}

	/**
	 * @param calificacion the calificacion to set
	 */
	public void setCalificacion(Double calificacion) {
		this.calificacion = calificacion;
	}

	/**
	 * @return the peso
	 */
	public Double getPeso() {
		return peso;
	}

	/**
	 * @param peso the peso to set
	 */
	public void setPeso(Double peso) {
		this.peso = peso;
	}

	
	


			

}
