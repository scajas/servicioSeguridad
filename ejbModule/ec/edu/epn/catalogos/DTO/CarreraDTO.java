package ec.edu.epn.catalogos.DTO;

import java.io.Serializable;


public class CarreraDTO implements Serializable{

	
	/**
	 * 
	 */
	private static final long serialVersionUID = 3574385754496084840L;
	private String idCarrera;
	private String nombCarrera;
	private String nombFacultad;
	private Integer idFacultad; 

	public CarreraDTO() {
	}

	public CarreraDTO(String idCarrera, String nombCarrera, String nombFacultad) {

		
	}
	
	/**GETTERS AND SETTERS*/


	/**
	 * @return the idCarrera
	 */
	public String getIdCarrera() {
		return idCarrera;
	}

	/**
	 * @param idCarrera the idCarrera to set
	 */
	public void setIdCarrera(String idCarrera) {
		this.idCarrera = idCarrera;
	}

	/**
	 * @return the nombCarrera
	 */
	public String getNombCarrera() {
		return nombCarrera;
	}

	/**
	 * @param nombCarrera the nombCarrera to set
	 */
	public void setNombCarrera(String nombCarrera) {
		this.nombCarrera = nombCarrera;
	}

	/**
	 * @return the nombFacultad
	 */
	public String getNombFacultad() {
		return nombFacultad;
	}

	/**
	 * @param nombFacultad the nombFacultad to set
	 */
	public void setNombFacultad(String nombFacultad) {
		this.nombFacultad = nombFacultad;
	}

	/**
	 * @return the idFacultad
	 */
	public Integer getIdFacultad() {
		return idFacultad;
	}

	/**
	 * @param idFacultad the idFacultad to set
	 */
	public void setIdFacultad(Integer idFacultad) {
		this.idFacultad = idFacultad;
	}
	
	
		

}
