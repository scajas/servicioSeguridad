package ec.edu.epn.silabo.dto;

import java.io.Serializable;

public class CordinacionDTO implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 5196229419172073103L;
	
	private String codcar;
	private String cargo;
	private String cedula;
	private String carrera;
	public String getCodcar() {
		return codcar;
	}
	public void setCodcar(String codcar) {
		this.codcar = codcar;
	}
	public String getCargo() {
		return cargo;
	}
	public void setCargo(String cargo) {
		this.cargo = cargo;
	}
	public String getCedula() {
		return cedula;
	}
	public void setCedula(String cedula) {
		this.cedula = cedula;
	}
	public String getCarrera() {
		return carrera;
	}
	public void setCarrera(String carrera) {
		this.carrera = carrera;
	}
	
	

}
