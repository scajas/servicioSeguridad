package ec.edu.epn.saew.Tesis;

import java.io.Serializable;

public class Tesis implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String CódigoProfesor;
	private String NroTesis;
	private String Tesis;
	private String CodigoEstudiante;
	private String DefensaOral;
	private String PeríodoGrado;
	private String Codcar;
	private String Carrera;
	private String Facultad;
	private String Nivel;
	public String getCódigoProfesor() {
		return CódigoProfesor;
	}
	public void setCódigoProfesor(String códigoProfesor) {
		CódigoProfesor = códigoProfesor;
	}
	public String getNroTesis() {
		return NroTesis;
	}
	public void setNroTesis(String nroTesis) {
		NroTesis = nroTesis;
	}
	public String getTesis() {
		return Tesis;
	}
	public void setTesis(String tesis) {
		Tesis = tesis;
	}
	public String getCodigoEstudiante() {
		return CodigoEstudiante;
	}
	public void setCodigoEstudiante(String codigoEstudiante) {
		CodigoEstudiante = codigoEstudiante;
	}
	public String getDefensaOral() {
		return DefensaOral;
	}
	public void setDefensaOral(String defensaOral) {
		DefensaOral = defensaOral;
	}
	public String getPeríodoGrado() {
		return PeríodoGrado;
	}
	public void setPeríodoGrado(String períodoGrado) {
		PeríodoGrado = períodoGrado;
	}
	public String getCodcar() {
		return Codcar;
	}
	public void setCodcar(String codcar) {
		Codcar = codcar;
	}
	public String getCarrera() {
		return Carrera;
	}
	public void setCarrera(String carrera) {
		Carrera = carrera;
	}
	public String getFacultad() {
		return Facultad;
	}
	public void setFacultad(String facultad) {
		Facultad = facultad;
	}
	public String getNivel() {
		return Nivel;
	}
	public void setNivel(String nivel) {
		Nivel = nivel;
	}
	

}
