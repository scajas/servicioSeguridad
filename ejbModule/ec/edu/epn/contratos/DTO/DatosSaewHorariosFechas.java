package ec.edu.epn.contratos.DTO;

public class DatosSaewHorariosFechas {
	
	
	//VARIABLES
	private String codCar;
	private String codMat;
	private String paralelo;
	private String hora1;
	private String hora2;
	private String fecha;
	
	//SETTERS Y GETTERS
	public String getCodCar() {
		return codCar;
	}
	public void setCodCar(String codCar) {
		this.codCar = codCar;
	}
	public String getCodMat() {
		return codMat;
	}
	public void setCodMat(String codMat) {
		this.codMat = codMat;
	}
	public String getParalelo() {
		return paralelo;
	}
	public void setParalelo(String paralelo) {
		this.paralelo = paralelo;
	}
	public String getHora1() {
		return hora1;
	}
	public void setHora1(String hora1) {
		this.hora1 = hora1;
	}
	public String getHora2() {
		return hora2;
	}
	public void setHora2(String hora2) {
		this.hora2 = hora2;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
}
